char	*ft_strchr(const char *s, int c)
{
	char	lt;
	int		i;

	i = 0;
	lt = (char )c;
	while (s[i] != lt && s[i] != '\0')
	{
		i++;
		s++;
	}
	if (s[i] == '\0')
		s++;
	return ((char *)s);
}
